def add(x, y):
    """Return sum of x and y"""
    return x + y

def subtract(x, y):
    """Return difference of x and y"""
    return x - y

def add3(x, y, z=0):
    """Return sum of x and y, and optionally z"""
    return x + y + z

def addsubtract(x, y):
    """Return sum and difference of x and y"""
    s = x + y
    d = x - y
    return s, d
